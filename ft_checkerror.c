int	ft_checkerror(char **tab, int x, int y, char c)
{
	int i;

	i = 0;
	if (y < 4)
		if (tab[x][y + 1] == '#')
			i++;
	if (x < 4)
		if (tab[x + 1][y] == '#')
			i++;
	if (y > 0)
		if (tab[x][y - 1] == '#')
			i++;
	if (x > 0)
		if (tab[x - 1][y] == '#')
			i++;
	return (i);
}
